    public UpdateItem() {
        initComponents();
        

        Library l = Library.getInstance();
        Item it = l.getItems().get(itemss.selected);
        int[] genresindex = new int[it.getGenres().length];
        int[] authorsindex = new int[it.getAuthors().length];
        if (l.getGenres().size() != 0) {
            String[] data = new String[l.getGenres().size()];

            int counter = 0;
            for (int i = 0; i < l.getGenres().size(); i++) {
                data[i] = l.getGenres().get(i).getName();
                for (int j = 0; j < it.getGenres().length; j++) {
                    if (l.getGenres().get(i).getName().equals(it.getGenres()[j])) {
                        genresindex[counter] = i;
                        counter++;
                    }
                }
            }
            this.genres.setListData(data);
        } else {
            this.err.setText("No Genres found");
        }

        if (l.getAuthors().size() != 0) {
            String[] data1 = new String[l.getAuthors().size()];
            int counter1 = 0;
            for (int i = 0; i < l.getAuthors().size(); i++) {
                data1[i] = l.getAuthors().get(i).getName();
                for (int j = 0; j < it.getAuthors().length; j++) {
                    if (l.getAuthors().get(i).getName().equals(it.getAuthors()[j])) {
                        authorsindex[counter1] = i;
                        counter1++;
                    }
                }
            }
            this.authors.setListData(data1);
        } else {
            this.err.setText("No Authors found");
        }

        this.isbn.setText(it.getISBN());
        this.name.setText(it.getName());
        this.pub.setText(it.getPublisher());
        this.date.setText(it.getPubDate());
        this.pages.setText(String.valueOf(it.getNumOfPages()));
        this.genres.setSelectedIndices(genresindex);
        this.authors.setSelectedIndices(authorsindex);

    }

