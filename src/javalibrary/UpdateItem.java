    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (this.isbn.getText().equals("") || this.name.getText().equals("") || this.pub.getText().equals("") || this.date.getText().equals("")
                || this.pages.getText().equals("")) {
            this.err.setText("Please fill all the fields");
        } else if (this.authors.getSelectedValuesList().size() == 0 || this.genres.getSelectedValuesList().size() == 0) {
            this.err.setText("Please choose one or multi genre or author");
        } else {
            String num = pages.getText();
            try {
                int val = Integer.parseInt(num);
                Library l = Library.getInstance();
                String[] authorss = new String[this.authors.getSelectedValuesList().size()];
                for (int i = 0; i < authors.getSelectedValuesList().size(); i++) {
                    authorss[i] = authors.getSelectedValuesList().get(i);
                }
                String[] genress = new String[this.genres.getSelectedValuesList().size()];
                for (int i = 0; i < genres.getSelectedValuesList().size(); i++) {
                    genress[i] = genres.getSelectedValuesList().get(i);
                }
                boolean result = l.updateItem(itemss.selected ,this.isbn.getText(), this.name.getText(), val, this.pub.getText(),
                        this.date.getText(), authorss, genress);
                if (result == true) {
                    this.err.setText("Item Updated Successfully");
                } else {
                    this.err.setText("Duplicate ISBN, try again");
                }
            } catch (NumberFormatException e) {
                this.err.setText("please, enter just numbers in Num of Pages field");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

